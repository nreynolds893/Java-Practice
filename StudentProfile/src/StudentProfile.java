
public class StudentProfile {
	String firstName;
	String lastName;
	int graduateYear;
	double gpa;
	String degree;
	
	public StudentProfile(String firstName, String lastName, int graduateYear, double gpa, String degree) {
		this.firstName = firstName;
		this.lastName = lastName;
		this.graduateYear = graduateYear;
		this.gpa = gpa;
		this.degree = degree;
	}
}
